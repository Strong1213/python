# Python Variables Example

# Integer variable
age = 21
print("Age:", age)

# Float variable
price = 99.99
print("Price:", price)

# String variable
name = "Raza"
print("Name:", name)

# Boolean variable
is_student = True
print("Is Student:", is_student)

# Multiple variables in one line
x, y, z = 10, 20, 30
print("x:", x, "y:", y, "z:", z)

